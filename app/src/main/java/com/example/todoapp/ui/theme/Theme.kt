private val LightColorScheme = lightColorScheme(
    primary = LightToDoColors().primary_color,
    onPrimary = LightToDoColors().black,
    secondary = LightToDoColors().separator_gray,
    tertiary = LightToDoColors().tertiary_gray
)

@Composable
fun ToDoAppTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable () -> Unit
) {
    val colorScheme = LightColorScheme

    MaterialTheme(
        colorScheme = colorScheme,
        typography = Typography,
        content = content
    )
}